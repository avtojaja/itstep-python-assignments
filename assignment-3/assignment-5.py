# 1. დაწერეთ პითონის პროგრამა, რომელიც დასაწყისში შექმნის ცარიელ სიას ([]), თუ მომხმარებელი შეიყვანს სიმბოლო 'a'-ს, ნიშნავს, რომ უნდა დაამატოთ სიაში რიცხვი; თუ აკრიფა 'r', სიიდან უნა წაიშალოს რიცხვი; 'e'-ს შეტანისას პროგრამამ უნდა დაასრულოს მუშაობა. მიღებული შედეგი დაბეჭდეთ კონსოლში.
# a – append
# r – remove
# e – exit
# გამოიყენეთ მხოლოდ ეს ბრძანებები და მოახდინეთ სიაზე ზემოქმედება.

# ცარიელი სია
numbers = []

while True:
    command = input("შეიყვანეთ ბრძანება (a - append, r - remove, e - exit): ").strip()

    if command == "a":
        value = int(input("შეიყვანეთ რიცხვი დასამატებლად: "))
        numbers.append(value)
        print("სია:", numbers)

    elif command == "r":
        if numbers:  # თუ სია ცარიელი არაა
            value = int(input("შეიყვანეთ რიცხვი წასაშლელად: "))
            if value in numbers:
                numbers.remove(value)
                print(f"{value} წაიშალა სიიდან.")
            else:
                print(f"{value} არ მოიძებნა სიაში!")
        else:
            print("სია ცარიელია, წაშლა შეუძლებელია!")
        print("სია:", numbers)

    elif command == "e":
        print("პროგრამა დასრულდა. საბოლოო სია:", numbers)
        break

    else:
        print("არასწორი ბრძანება! გამოიყენეთ a, r ან e.")



