# 3. დაწერეთ პითონის პროგრამა, რომელიც მიიღებს ტელეფონის ნომერს და regex-ით შეამოწმებს შეყვანილი ნომერი იცავს თუ არა "(123) 456-789" ფორმატს, თუ იცავს დააბრუნეთ შეყვანილი ტელეფონის ნომერი, წინააღმდეგ შემთხვევაში გამოიტანეთ "Invalid format" ტექსტი.
# მინიშნება: სრული დამთხვევისთვის გამოიყენეთ .fullmatch() მეთოდი re მოდულიდან.

import re

# მომხმარებლისგან ტელეფონის ნომრის მიღება
phone = input("შეიყვანეთ ტელეფონის ნომერი ფორმატით: (123) 456-789: ")

# რეგულარული გამოსახულება "(123) 456-789" ფორმატისთვის
pattern = r"\(\d{3}\) \d{3}-\d{3}"

# სრული დამთხვევის შემოწმება
if re.fullmatch(pattern, phone):
    print("ტელეფონის ნომრის ფორმატი სწორია:", phone)
else:
    print("ტელეფონის ნომრის ფორმატი არასწორია")
