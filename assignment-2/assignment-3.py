# 1. დაწერეთ პითონის პროგრამა, რომელიც მიიღებს მომხმარებლისგან რიცხვს "n" და ბეჭდავს 1-დან "n"-მდე რიცხვების ჯამს.

# მომხმარებლისგან რიცხვის მიღება
n = int(input("შეიყვანეთ რიცხვი n: "))

# ჯამის დათვლა
sum = 0
for i in range(1, n):
    sum += i

# შედეგის დაბეჭდვა
print(f"1-დან {n}-მდე რიცხვების ჯამია:", sum)



# 2. დაწერეთ პითონის პროგრამა, რომელიც მიიღებს მომხმარებლისგან რიცხვს და შემდეგ იყენებს "while" ციკლს რომ რევერსულად დაბეჭდოს რიცხვები 0-მდე. მაგალითად თუ შეიყვანს 4, დაიბეჭდოს 4, 3, 2, 1

# მომხმარებლისგან რიცხვის მიღება
n = int(input("შეიყვანეთ რიცხვი: "))

# while ციკლით უკუღმა ბეჭდვა
while n > 0:
    print(n)
    n -= 1



# 3. დაწერეთ პითონის პროგრამა თამაშისთვის, რომელიც მუდმივად სთხოვს მომხმარებელს გამოიცნოს წინასწარ განსაზღვრული რიცხვი. როდესაც მომხმარებელი გამოიცნობს სწორ რიცხვს, დაასრულებს პროგრამა მუშაობას.

import random

# შემთხვევითი რიცხვის შერჩევა 1-დან 100-მდე
secret_number = random.randint(1, 100)

while True:
    guess = int(input("გამოიცანით რიცხვი (1-100): "))

    if guess == secret_number:
        print("გილოცავთ! თქვენ გამოიცანით სწორი რიცხვი!")
        break
    elif guess < secret_number:
        print("სცადეთ უფრო დიდი რიცხვი!")
    else:
        print("სცადეთ უფრო პატარა რიცხვი!")



# 4. დაწერეთ პითონის პროგრამა, რომელიც მიიღებს მუდმივად რიცხვებს. შექმენით საწყისი ცვლადი total_sum = 0, შეამოწმეთ რიცხვი თუ დადებითია, მხოლოდ მაშინ დაუმატეთ total_sum ცვლადს. ეს პროცესი გაგრძელდეს იქამდე სანამ მომხმარებელი არ შეიყვანს 'sum' ტექსტს, რის შემდეგაც დაიბეჭდება შეყვანილი დადებითი რიცხვების ჯამი.

# საწყისი ჯამი
total_sum = 0

while True:
    user_input = input("შეიყვანეთ რიცხვი ან 'sum' ჯამის სანახავად: ")

    if user_input.lower() == "sum":
        print("დადებითი რიცხვების ჯამია:", total_sum)
        break

    try:
        number = int(user_input)
        if number > 0:
            total_sum += number
    except ValueError:
        print("გთხოვთ შეიყვანოთ რიცხვი ან 'sum'.")
