# 1. კონსოლიდან შეიტანეთ მიმდევრობა. დაბეჭდეთ უნიკალური მონაცემებიანი სიმრავლე (set).

# კონსოლიდან სტრიქონის შეყვანა
data = input("შეიყვანეთ მონაცემები (space-ებით გამოყოფილი): ")

# სიად გარდაქმნა
items = data.split()

# უნიკალური მნიშვნელობების სიმრავლე
unique_items = set(items)

# შედეგის დაბეჭდვა
print("უნიკალური მონაცემები:", unique_items)



# 2. პირობა იგივეა, რაც პირველ დავალებაში, ოღონდ დაბეჭდეთ უნიკალური მონაცემებიანი სიმრავლე, რომლის შეცვლაც შეუძლებელი იქნება (frozenset).

# კონსოლიდან სტრიქონის შეყვანა
data = input("შეიყვანეთ მონაცემები (space-ებით გამოყოფილი): ")

# სიად გარდაქმნა
items = data.split()

# უნიკალური მნიშვნელობების უცვლელი სიმრავლე
unique_items = frozenset(items)

# შედეგის დაბეჭდვა
print("უცვლელი უნიკალური სიმრავლე:", unique_items)



# 3. აიღეთ set ტიპის ორი მონაცემი. ელემენტები თავად განსაზღვრეთ. დაბეჭდეთ გაერთიანებული მონაცემები კორტეჟის სახით (tuple).

# ორი set
set1 = {1, 2, 3, 4}
set2 = {3, 4, 5, 6}

# გაერთიანება
combined = set1.union(set2)

# კორტეჟად გარდაქმნა
result = tuple(combined)

# შედეგის დაბეჭდვა
print(result)



# 4. კონსოლიდან შევიტანოთ რიცხვების მიმდევრობა როგორც კორტეჟი (tuple). დავბეჭდოთ მხოლოდ უნიკალური ელემენტები სიის სახით (list).

# კონსოლიდან შემოტანა
numbers = input("შეიყვანეთ რიცხვები space-ებით: ")

# კორტეჟად გარდაქმნა
tpl = tuple(numbers.split())

# უნიკალური ელემენტები
unique_list = list(set(tpl))

# შედეგის დაბეჭდვა
print(unique_list)



# 5. მოცემულია სია, რომლის ელემენტები წარმოადგენენ კორტეჟს:
# [("Gega", 24), ("Gaga", 21), ("Goga", 19), ("Giga", 27), ("Gagi", 11)]

